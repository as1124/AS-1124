public class LovedHeros {

	private String name;
	
	private String story;
	
	public LovedHeros() {
		// default constructor
	}

	/**
	 * @return Returns the {@link name}.
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name The {@link name} to set.
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return Returns the {@link story}.
	 */
	public String getStory() {
		return story;
	}

	/**
	 * @param story The {@link story} to set.
	 */
	public void setStory(String story) {
		this.story = story;
	}
	
}
